	pbAddPokemonSilent(:DITTO,5)
	pbAddPokemonSilent(:DITTO,5)
	pbAddPokemonSilent(:DITTO,5)
	pbAddPokemonSilent(:DITTO,5)
end

def pbStoryModeRemoveDummyStarters # Clear out the dummy team, once the PSS has been filled with all 1025+ Pokemon
	# Normally, this is not a good way to remove a specfic Pokemon. But in this context, this does exactly what is required
	$player.party.delete_at(5)
	$player.party.delete_at(4)
	$player.party.delete_at(3)
	$player.party.delete_at(2)
	$player.party.delete_at(1)
	$player.party.delete_at(0)
end 

